(function () { "use strict";
var samples = {};
samples.positioning = {};
samples.positioning.Main = function() {
	this._mainContext = famous.core.Engine.createContext();
	var lightSquareProperties = { };
	lightSquareProperties.backgroundColor = "#AAAAAA";
	lightSquareProperties.color = "#000000";
	lightSquareProperties.textAlign = "center";
	var lightSquareOptions = { };
	lightSquareOptions.content = "Light Square";
	lightSquareOptions.size = [100,100];
	lightSquareOptions.properties = lightSquareProperties;
	var lightSquare = new famous.core.Surface(lightSquareOptions);
	var darkSquareProperties = { };
	darkSquareProperties.backgroundColor = "#666666";
	darkSquareProperties.color = "#DDDDDD";
	darkSquareProperties.textAlign = "center";
	var darkSquareOptions = { };
	darkSquareOptions.content = "Dark Square";
	darkSquareOptions.size = [100,100];
	darkSquareOptions.properties = darkSquareProperties;
	var darkSquare = new famous.core.Surface(darkSquareOptions);
	var centerCircleProperties = { };
	centerCircleProperties.border = "1px solid #DDDDDD";
	centerCircleProperties.borderRadius = "7px";
	var centerCircleOptions = { };
	centerCircleOptions.size = [15,15];
	centerCircleOptions.properties = centerCircleProperties;
	var centerCircle = new famous.core.Surface(centerCircleOptions);
	var horizontalRule = new famous.core.Surface({ size : [window.innerWidth,1], properties : { backgroundColor : "#FFFFFF"}});
	var verticalRule = new famous.core.Surface({ size : [1,window.innerHeight], properties : { backgroundColor : "#FFFFFF"}});
	var originModifier = new famous.modifiers.StateModifier({ origin : [0.5,0.5], align : [0.5,0.5]});
	var alignModifier = new famous.modifiers.StateModifier({ align : [0.5,0.5]});
	var alignOriginModifier = new famous.modifiers.StateModifier({ align : [0.5,0.5], origin : [1,1]});
	this._mainContext.add(alignModifier).add(lightSquare);
	this._mainContext.add(alignOriginModifier).add(darkSquare);
	var node = this._mainContext.add(originModifier);
	node.add(verticalRule);
	node.add(horizontalRule);
	node.add(centerCircle);
};
samples.positioning.Main.main = function() {
	new samples.positioning.Main();
};
samples.positioning.Main.main();
})();
